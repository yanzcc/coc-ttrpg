def get_weapon_by_name(name: str) -> dict | None:
    """根据名称查找武器，支持部分匹配"""
    for category, weapons in ALL_WEAPONS.items():
        for weapon in weapons:
            if name in weapon["name"] or weapon["name"] in name:
                return {**weapon, "category": category}
    return None


def get_weapons_by_skill(skill: str) -> list[dict]:
    """根据所用技能查找武器"""
    results = []
    for category, weapons in ALL_WEAPONS.items():
        for weapon in weapons:
            if skill in weapon["skill"]:
                results.append({**weapon, "category": category})
    return results


def get_weapons_by_category(category: str) -> list[dict]:
    """根据武器类别获取所有武器"""
    return ALL_WEAPONS.get(category, [])


def get_all_categories() -> list[str]:
    """获取所有武器类别名称"""
    return list(ALL_WEAPONS.keys())


